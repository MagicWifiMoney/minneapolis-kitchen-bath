      <p>
        This is where wet rooms succeed or fail. The entire bathroom floor has to slope to a drain at a minimum of 1/4 inch per foot. In a 10x10 wet room, that&apos;s a non-trivial amount of slope to hide. We typically use one of three drainage strategies:
      </p>
